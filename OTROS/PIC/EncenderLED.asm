;CONFIGURACION

DATO EQU 0X21
COUNT EQU 0X01

;INICIO DE PROGRAMA
	ORG 0X00		;POSICION INICIAL DE MEMORIA
	GOTO START

;CODIGO
START
	BSF	STATUS, 5
	BCF	TRISB, 0
	BCF	STATUS, RP0
	MOVLW	0X00
	MOVWF	PORTB
	GOTO	MENU

MENU
	CALL 	ENCENDIDO
	CALL	DELAY
	CALL 	APAGADO
	CALL	DELAY
	GOTO 	MENU

ENCENDIDO
	MOVLW	1
	MOVWF	DATO
	CALL	MV
	RETURN

APAGADO
	MOVLW	0
	MOVWF	DATO
	CALL	MV
	RETURN

MV
	MOVF	DATO, W
	MOVWF	PORTB
	RETURN

;================DELAY================
DELAY:
	MOVLW	.100
	MOVWF	COUNT

LOOP2:
	MOVLW	-.39
	MOVWF	TMR0
	BCF	INTCON,T0IF
LOOP:
	BTFSS	INTCON,T0IF
	GOTO	LOOP

	DECFSZ	COUNT,F
	GOTO	LOOP2
	RETLW	0
;=====================================

END
;CONFIGURACION

DATO EQU 0X21

;INICIO DE PROGRAMA
	ORG 0X00		;POSICION INICIAL DE MEMORIA
	GOTO START

;CODIGO
START
	BSF	STATUS, 5 	;CAMBIO A BANCO 1, POR DIRECCION
	CLRF	TRISB		;TODOS SON SALIDA
	BCF	STATUS, RP0 	;CAMBIO A BANCO 0, POR NOMBRE
	BCF	STATUS, 5 	;CAMBIO A BANCO 0, POR DIRECCION
	MOVLW	0X00
	MOVWF	PORTB
	GOTO	MENU

MENU
	CALL MV1
	CALL MV2
	GOTO MENU

MV
	MOVF	DATO, W
	MOVWF	PORTB
	RETURN

MV1
	MOVLW	B'00000001'
	MOVWF	DATO
	CALL	MV
	RETURN

MV2
	MOVLW	B'00000010'
	MOVWF	DATO
	CALL	MV
	RETURN
END